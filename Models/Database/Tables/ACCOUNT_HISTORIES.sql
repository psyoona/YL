IF (OBJECT_ID('dbo.ACCOUNT_HISTORIES') IS NOT NULL) AND (OBJECTPROPERTY(OBJECT_ID('dbo.ACCOUNT_HISTORIES'), 'IsUserTable')=1)
	DROP TABLE dbo.ACCOUNT_HISTORIES
GO

CREATE TABLE dbo.ACCOUNT_HISTORIES
(
	ACCOUNT_HISTORY_NO	INT		IDENTITY(1,1) NOT NULL
	,ACCOUNT_NO		NVARCHAR(50)	NOT NULL
	,[DESCRIPTION]		NVARCHAR(100)	NOT NULL
	,DELTA			INT		NOT NULL
	,TEMP_BALANCE		INT		NOT NULL
	,USING_DATE		DATETIME	NOT NULL
)
GO

-- Primary Key
ALTER TABLE dbo.ACCOUNT_HISTORIES WITH NOCHECK ADD
	CONSTRAINT PK_ACCOUNT_HISTORIES PRIMARY KEY CLUSTERED (ACCOUNT_HISTORY_NO) ON [PRIMARY]
GO

