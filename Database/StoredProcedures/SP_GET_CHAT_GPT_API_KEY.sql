	DROP PROCEDURE SP_GET_CHAT_GPT_API_KEY
GO

CREATE PROCEDURE SP_GET_CHAT_GPT_API_KEY
	@USING_KEY			NVARCHAR(128)
WITH ENCRYPTION
AS
SET NOCOUNT ON;

