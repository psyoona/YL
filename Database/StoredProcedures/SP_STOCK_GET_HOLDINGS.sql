CREATE OR ALTER PROCEDURE SP_STOCK_GET_HOLDINGS
WITH ENCRYPTION
AS
SET NOCOUNT ON;

SELECT	H.ID
	,H.STOCK_CODE
	,H.STOCK_NAME
	,H.QUANTITY
	,H.AVG_BUY_PRICE
	,H.CURRENT_PRICE
	,H.PROFIT_LOSS_RATE
	,H.BOUGHT_AT
	,H.UPDATED_AT
FROM	dbo.HOLDINGS H
WHERE	H.QUANTITY > 0
ORDER BY H.STOCK_NAME;

SET NOCOUNT OFF;
GO
