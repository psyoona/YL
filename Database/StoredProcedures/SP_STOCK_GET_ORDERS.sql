CREATE OR ALTER PROCEDURE SP_STOCK_GET_ORDERS
WITH ENCRYPTION
AS
SET NOCOUNT ON;

SELECT	ID
	,ORDER_NO
	,STOCK_CODE
	,STOCK_NAME
	,ORDER_TYPE
	,ORDER_PRICE
	,ORDER_QUANTITY
	,FILLED_QUANTITY
	,FILLED_PRICE
	,ORDER_STATUS
	,REASON
	,CREATED_AT
	,UPDATED_AT
FROM	dbo.ORDERS
ORDER BY CREATED_AT DESC;

SET NOCOUNT OFF;
GO
