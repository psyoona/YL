IF (OBJECT_ID('dbo.ALBUM_USERS') IS NOT NULL) AND (OBJECTPROPERTY(OBJECT_ID('dbo.ALBUM_USERS'), 'IsUserTable')=1)
	DROP TABLE dbo.ALBUM_USERS
GO

CREATE TABLE ALBUM_USERS
(
	PHONE_NUMBER		NVARCHAR(20)	NOT NULL
	,PASSWORD_HASH		NVARCHAR(128)	NOT NULL
	,[USER_NAME]		NVARCHAR(50)	NULL
	,IS_ACTIVE		BIT		NOT NULL DEFAULT 1
	,CREATED_DATE		DATETIME	NOT NULL DEFAULT GETDATE()
	,LAST_LOGIN_DATE	DATETIME	NULL
);

-- Primary Key
ALTER TABLE dbo.ALBUM_USERS WITH NOCHECK ADD
	CONSTRAINT PK_ALBUM_USERS PRIMARY KEY CLUSTERED (PHONE_NUMBER) ON [PRIMARY]
GO

